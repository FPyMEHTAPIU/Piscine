int	ft_sqrt(int nb)
{
	int	num;

	num = 1;
	if (nb > 0)
	{
		while (num * num <= nb)
		{
			if ((num * num) == nb)
			{
				return (num);
			}
			if (num > 46340)
			{
				return (0);
			}
			num++;
		}
	}
	return (0);
}
